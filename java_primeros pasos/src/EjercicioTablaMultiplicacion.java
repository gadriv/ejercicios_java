
public class EjercicioTablaMultiplicacion {

	public static void main(String[] args) {

		for (int contador = 0; contador <= 10; contador++) {
			for (int tablaMultiplicar = 0; tablaMultiplicar <= 10; tablaMultiplicar++) {
				System.out.print(contador * tablaMultiplicar);
				System.out.print(" ");
			}
			System.out.println();
		}
	}

}
